  String getQuoteForToday(String userId, DateTime date) {
    if (_quotes.isEmpty) return "Have a wonderful day!";

    // Create a deterministic index ensuring the same user sees the same quote on the same day,
    // but different users might see different quotes (depending on the hash).
    // Using day of year to rotate daily.
    final dayOfYear = int.parse(
      "${date.year}${date.month.toString().padLeft(2, '0')}${date.day.toString().padLeft(2, '0')}",
    );
    final seed = userId.hashCode + dayOfYear;
    final index = seed.abs() % _quotes.length;

    return _quotes[index];
  }

